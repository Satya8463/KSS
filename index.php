<font color="blue"><h2>Introduction</h2></font>
Knowledge   sharing   is   a   social   communication   culture  including,  knowledge,  experiences  and  skills  throughout  the  whole  organization.
Knowledge  sharing  takes  place  at  the  both  the  individual  and  organizational  levels.
It  helps  in  terminating  the  knowledge  that  exists  within  the  organization  and  making  the  knowledge  accessible  to  others  in  the  business
<h5>Purpose-:</h5>
The  actual  design  of the  various  modules  and  components  of  knowledge  sharing is  who  is  in  search  of  knowledge,  can  use  the  system  and  download  the  relevant  docs.
<h5>Product scope-:</h5>
Knowledge  sharing  system  is  an  web  application  for  those  who  want  to  seek  a knowledge,  faculty  with  expertise  in  different  skill  can  share  their  knowledge  and  admin  who  will  control  the  whole  system.
The  major  advantage  of  this  portal/application  is  that  student  who  wants  to  seek  knowledge  sitting  from  long  distance  can  access  the  knowledge  easily.
Knowledge  sharing  system  application  is  to  contribute  or  share  information  in  the  form  file  upload    (doc/ppt/pdf)  only

